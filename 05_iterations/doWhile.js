// while loop
let index = 0;
while(index <= 20)
{
    // console.log(`The value is ${index}`);
    index = index + 2;
}

let myHeroes = ["Ram","Krishna","Hanuman","Ganesha","Shiv"];
let hero = 0;
while(hero < myHeroes.length)
{
    // console.log(`My hero is ${myHeroes[hero]}`);
    hero = hero + 1;
}
